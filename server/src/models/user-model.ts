import mongoose from "mongoose";

const UserSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  googleId: {
    type: String,
    select: false,
  },
});

const UserModel = mongoose.model("user", UserSchema);

export default UserModel;
